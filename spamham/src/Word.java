
public class Word {
	private String s;
	private int count;
	
	public Word(String s) {
		super();
		this.s = s;
		this.count=1;
	}
	public String getS() {
		return s;
	}
	public void setS(String s) {
		this.s = s;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}

}
